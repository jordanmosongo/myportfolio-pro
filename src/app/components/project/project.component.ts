  slides: any = [[]];
  chunk(arr : any, chunkSize : number) {
    let R = [];
    for (let i = 0, len = arr.length; i < len; i += chunkSize) {
      R.push(arr.slice(i, i + chunkSize));
    }
    return R;
  }
  constructor() { }

  ngOnInit(): void {
    this.slides = this.chunk(this.cards, 3);
  }

